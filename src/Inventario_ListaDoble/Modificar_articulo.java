    private void articulo_modificado(String Nombre, String Descripcion, String cod, int Unidades, double Precio) {
        try (Connection conexion = DriverManager.getConnection("jdbc:mysql://localhost:3306/inventario?autoReconnect=true&useSSL=false", "root", "")) {
            String sentenciaSQL = "UPDATE articulo SET Nombre = ?, Descripcion = ?, Unidades = ?, Precio = ? WHERE Codigo = ?";
            try (PreparedStatement sentencia = conexion.prepareStatement(sentenciaSQL)) {
                sentencia.setString(1, Nombre);
                sentencia.setString(2, Descripcion);
                sentencia.setInt(3, Unidades);
                sentencia.setDouble(4, Precio);
                sentencia.setString(5, cod);
                int filasModificadas = sentencia.executeUpdate();
                System.out.println(filasModificadas);
            }
        } catch (SQLException e) {
            System.out.println("Error al modificar el registro: " + e.getMessage());
        }
    }
