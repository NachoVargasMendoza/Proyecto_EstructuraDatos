    private void ModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ModificarActionPerformed
        try {
            // Obtener los valores de entrada
            Integer nuevasUnidades = null;
            Double nuevoPrecio = null;
            String nuevoNombre = null;
            String nuevaDescripcion = null;
            String cod = getCodigoSeleccionado();

            // Verificar si cada checkbox está seleccionado y actualizar los valores correspondientes
            if (!nombreBloqueado) {
                nuevoNombre = this.Nombre_modificado.getText();
                if (nuevoNombre == null || nuevoNombre.trim().isEmpty()) {
                    throw new IllegalArgumentException("El nombre del artículo no puede ser null o una cadena vacía");
                }
            }else {
                // Desactivar el campo y cambiar el color de fondo  
                nuevoNombre=getNombreSeleccionado();
                this.Nombre_modificado.setEnabled(false);
                this.Nombre_modificado.setBackground(Color.LIGHT_GRAY);
            }
            if (!unidadesBloqueadas) {
                nuevasUnidades = Integer.parseInt(this.Unidades_modificadas.getText());
                if (nuevasUnidades <= 0) {
                    throw new IllegalArgumentException("El valor de unidades debe ser mayor que cero");
                }
            }else {
                // Desactivar el campo y cambiar el color de fondo
                nuevasUnidades=getUnidadesSeleccionado();
                this.Unidades_modificadas.setEnabled(false);
                this.Unidades_modificadas.setBackground(Color.LIGHT_GRAY);
            }
            if (!precioBloqueado) {
                nuevoPrecio = Double.parseDouble(this.Precio_modificado.getText());
                if (nuevoPrecio <= 0) {
                    throw new IllegalArgumentException("El valor de precio debe ser mayor que cero");
                }
            }else {
                // Desactivar el campo y cambiar el color de fondo
                nuevoPrecio=getPrecioSeleccionado();
                this.Precio_modificado.setEnabled(false);
                this.Precio_modificado.setBackground(Color.LIGHT_GRAY);
            }
            if (!descripcionBloqueada) {
                nuevaDescripcion = this.Descripcion_modificada.getText();
                if (nuevaDescripcion == null) {
                    throw new IllegalArgumentException("La Descripción del artículo no puede ser null o una cadena vacía");
                }
            }else {
                // Desactivar el campo y cambiar el color de fondo
                nuevaDescripcion=getDescripcionSeleccionado();
                this.Descripcion_modificada.setEnabled(false);
                this.Descripcion_modificada.setBackground(Color.LIGHT_GRAY);
            }
             // Modificar el artículo
            articulo_modificado(nuevoNombre, nuevaDescripcion, cod, nuevasUnidades, nuevoPrecio);
            JOptionPane.showMessageDialog(this, "El artículo ha sido modificado con éxito.", "Modificación exitosa", JOptionPane.INFORMATION_MESSAGE);
            
            this.setVisible(false);
            Eliminar_articulo_ c=new Eliminar_articulo_();
            // Actualizar la tabla en la interfaz de usuario existente
            c.cargar_tabla();
            c.setVisible(true);
        } catch (NumberFormatException ex) {
            // Mostrar un mensaje de error al usuario
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Error de entrada", JOptionPane.ERROR_MESSAGE);
        }catch(IllegalArgumentException | NullPointerException ex){
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);    
        }                       
    }//GEN-LAST:event_ModificarActionPerformed

